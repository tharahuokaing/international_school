// бһ“бһ¶бҹҶбһ…бһјбһӣ (Import) бһҳбҹүбһјбһҢбһ»бһӣ бһ“бһ·бһ„ Client бһ–бһё Zoom Video SDK
import ZoomVideo from '@zoom/videosdk';

const client = ZoomVideo.createClient();

/**
 * бһҳбһ»бһҒбһ„бһ¶бһҡбһҹбһҳбҹ’бһҡбһ¶бһ”бҹӢбһ’бһ¶бһ“бһ¶бһҗбһ¶бһ”бҹ’бһҡбһ–бҹҗбһ“бҹ’бһ’бһҹбҹҶбһЎбҹҒбһ„бһҸбҹ’бһҡбһјбһңбһ”бһ¶бһ“бһ—бҹ’бһҮбһ¶бһ”бҹӢ бһ“бһ·бһ„бһҠбҹҶбһҺбһҫбһҡбһҖбһ¶бһҡ (Speaker/Audio Activation)
 * бһ…бҹҶбһҺбһ¶бҹҶбҹ– бһҖбһҳбҹ’бһҳбһңбһ·бһ’бһёбһҡбһ»бһҖбһҡбһҖ (Browsers) бһҳбһҪбһҷбһ…бҹҶбһ“бһҪбһ“бһҸбһҳбҹ’бһҡбһјбһңбһұбҹ’бһҷбһҳбһ¶бһ“бһҖбһ¶бһҡбһ…бһ»бһ…бһ–бһёбһўбҹ’бһ“бһҖбһ”бҹ’бһҡбһҫбһ”бҹ’бһҡбһ¶бһҹбҹӢбһҮбһ¶бһҳбһ»бһ“бһҹбһ·бһ“ бһ‘бһҫбһ”бһўбһ“бһ»бһүбҹ’бһүбһ¶бһҸбһұбҹ’бһҷбһҹбҹҶбһЎбҹҒбһ„бһ”бһ“бҹ’бһӣбһәбһЎбһҫбһ„
 */
async function ensureAudioIsReady() {
    try {
        const mediaStream = client.getMediaStream();
        
        // бһ–бһ·бһ“бһ·бһҸбҹ’бһҷбһҳбһҫбһӣбһҗбһ¶бһҸбһҫбһ”бҹ’бһҡбһ–бҹҗбһ“бҹ’бһ’бһҹбҹҶбһЎбҹҒбһ„бһҸбҹ’бһҡбһјбһңбһ”бһ¶бһ“бһ—бҹ’бһҮбһ¶бһ”бҹӢбһҡбһҪбһ…бһҡбһ¶бһӣбҹӢбһ бһҫбһҷбһ¬бһ“бҹ…
        if (!mediaStream.isAudioSubscribed()) {
            await mediaStream.subscribeAudio();
            console.log("рҹ”Ҡ бһ”бҹ’бһҡбһ–бҹҗбһ“бҹ’бһ’бһҹбҹҶбһЎбҹҒбһ„бһҸбҹ’бһҡбһјбһңбһ”бһ¶бһ“бһ—бҹ’бһҮбһ¶бһ”бҹӢ бһ“бһ·бһ„бһҡбһҪбһ…бһҡбһ¶бһӣбҹӢбһҹбһҳбҹ’бһҡбһ¶бһ”бҹӢбһҖбһ¶бһҡбһҹбҹ’бһҸбһ¶бһ”бҹӢбҹ”");
        }
    } catch (error) {
        console.error("бһҳбһ¶бһ“бһ”бһүбҹ’бһ бһ¶бһҖбҹ’бһ“бһ»бһ„бһҖбһ¶бһҡбһ—бҹ’бһҮбһ¶бһ”бҹӢбһ”бҹ’бһҡбһ–бҹҗбһ“бҹ’бһ’бһҹбҹҶбһЎбҹҒбһ„бҹ–", error);
    }
}

/**
 * бһҳбһ»бһҒбһ„бһ¶бһҡбһҹбһҳбҹ’бһҡбһ¶бһ”бҹӢбһ”бһ·бһ‘ бһ¬ бһ”бһҫбһҖбһҳбһёбһҖбҹ’бһҡбһјбһ бҹ’бһңбһјбһ“бһҡбһ”бһҹбҹӢбһҒбҹ’бһӣбһҪбһ“бһҜбһ„ (Toggle Mute/Unmute)
 * @param {boolean} isMuted - бһ”бһүбҹ’бһҮбһјбһ“бһҸбһҳбҹ’бһӣбҹғ true бһ”бһҫбһ…бһ„бҹӢбһ”бһ·бһ‘бһҹбҹҶбһЎбҹҒбһ„ бһ“бһ·бһ„ false бһ”бһҫбһ…бһ„бҹӢбһ”бһҫбһҖбһҹбҹҶбһЎбҹҒбһ„
 */
async function toggleMyMicrophone(isMuted) {
    try {
        const mediaStream = client.getMediaStream();
        if (isMuted) {
            await mediaStream.muteAudio();
            console.log("рҹҺҷпёҸ бһҳбһёбһҖбҹ’бһҡбһјбһ бҹ’бһңбһјбһ“бһҸбҹ’бһҡбһјбһңбһ”бһ¶бһ“бһ”бһ·бһ‘ (Muted)бҹ”");
        } else {
            await mediaStream.unmuteAudio();
            console.log("рҹҺҷпёҸ бһҳбһёбһҖбҹ’бһҡбһјбһ бҹ’бһңбһјбһ“бһҸбҹ’бһҡбһјбһңбһ”бһ¶бһ“бһ”бһҫбһҖ (Unmuted)бҹ”");
        }
    } catch (error) {
        console.error("бһҳбһ¶бһ“бһ”бһүбҹ’бһ бһ¶бһҖбҹ’бһ“бһ»бһ„бһҖбһ¶бһҡбһ•бҹ’бһӣбһ¶бһҹбҹӢбһ”бҹ’бһҸбһјбһҡбһҹбҹ’бһҗбһ¶бһ“бһ—бһ¶бһ–бһҳбһёбһҖбҹ’бһҡбһјбһ бҹ’бһңбһјбһ“бҹ–", error);
    }
}

export { ensureAudioIsReady, toggleMyMicrophone };
