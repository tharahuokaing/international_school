// бһ“бһ¶бҹҶбһ…бһјбһӣ (Import) бһҳбҹүбһјбһҢбһ»бһӣ бһ“бһ·бһ„ Client бһ–бһё Zoom Video SDK
import ZoomVideo from '@zoom/videosdk';

const client = ZoomVideo.createClient();
let mediaStream;

/**
 * бһҳбһ»бһҒбһ„бһ¶бһҡбһҹбһҳбҹ’бһҡбһ¶бһ”бҹӢбһ…бһ¶бһ”бҹӢбһ•бҹ’бһҸбһҫбһҳбһ…бҹӮбһҖбһҡбҹҶбһӣбҹӮбһҖбһўбҹҒбһҖбҹ’бһҡбһ„бҹӢ (Start Screen Share)
 * @param {HTMLCanvasElement} shareCanvasElement - бһ’бһ¶бһҸбһ» Canvas бһ“бҹ…бһҖбҹ’бһ“бһ»бһ„ HTML бһҹбһҳбҹ’бһҡбһ¶бһ”бҹӢбһ”бһ„бҹ’бһ бһ¶бһүбһ‘бһ·бһҠбҹ’бһӢбһ—бһ¶бһ–бһўбҹҒбһҖбҹ’бһҡбһ„бҹӢбһҠбҹӮбһӣбһҖбҹҶбһ–бһ»бһ„бһ…бҹӮбһҖбһҡбҹҶбһӣбҹӮбһҖ
 */
async function startScreenSharing(shareCanvasElement) {
    try {
        mediaStream = client.getMediaStream();
        
        // бһ–бһ·бһ“бһ·бһҸбҹ’бһҷбһҳбһҫбһӣбһҗбһ¶бһҸбһҫбһҳбһ¶бһ“бһ“бһҡбһҺбһ¶бһҳбҹ’бһ“бһ¶бһҖбҹӢбһҖбҹҶбһ–бһ»бһ„бһ…бҹӮбһҖбһҡбҹҶбһӣбҹӮбһҖбһўбҹҒбһҖбҹ’бһҡбһ„бҹӢбһҡбһҪбһ…бһ бһҫбһҷбһ¬бһ“бҹ…
        if (!mediaStream.isStartShareLocked()) {
            // бһ…бһ¶бһ”бҹӢбһ•бҹ’бһҸбһҫбһҳбһҖбһ¶бһҡбһ…бҹӮбһҖбһҡбҹҶбһӣбҹӮбһҖбһўбҹҒбһҖбҹ’бһҡбһ„бҹӢбһ‘бҹ…бһҖбһ¶бһ“бҹӢ Session
            await mediaStream.startShare(shareCanvasElement);
            console.log("рҹ–ҘпёҸ бһҖбҹҶбһ–бһ»бһ„бһ…бҹӮбһҖбһҡбҹҶбһӣбҹӮбһҖбһўбҹҒбһҖбҹ’бһҡбһ„бҹӢбһҡбһ”бһҹбҹӢбһўбҹ’бһ“бһҖбһ‘бҹ…бһҖбһ¶бһ“бҹӢбһҗбҹ’бһ“бһ¶бһҖбҹӢбһҡбҹҖбһ“...");
        } else {
            console.warn("вҡ пёҸ бһҳбһ·бһ“бһўбһ¶бһ…бһ…бҹӮбһҖбһҡбҹҶбһӣбҹӮбһҖбһ”бһ¶бһ“бһ‘бҹҒ бһ–бҹ’бһҡбҹ„бҹҮбһҳбһ¶бһ“бһўбҹ’бһ“бһҖбһ•бҹ’бһҹбҹҒбһ„бһҖбҹҶбһ–бһ»бһ„бһ”бҹ’бһҡбһҫбһ”бҹ’бһҡбһ¶бһҹбҹӢбһҳбһ»бһҒбһ„бһ¶бһҡбһ“бҹҒбҹҮбҹ”");
        }
    } catch (error) {
        console.error("бһҳбһ¶бһ“бһ”бһүбҹ’бһ бһ¶бһҖбҹ’бһ“бһ»бһ„бһҖбһ¶бһҡбһ…бҹӮбһҖбһҡбҹҶбһӣбҹӮбһҖбһўбҹҒбһҖбҹ’бһҡбһ„бҹӢбҹ–", error);
    }
}

/**
 * бһҳбһ»бһҒбһ„бһ¶бһҡбһҹбһҳбҹ’бһҡбһ¶бһ”бҹӢбһ”бһүбҹ’бһҲбһ”бҹӢбһҖбһ¶бһҡбһ…бҹӮбһҖбһҡбҹҶбһӣбҹӮбһҖбһўбҹҒбһҖбҹ’бһҡбһ„бҹӢ (Stop Screen Share)
 */
async function stopScreenSharing() {
    try {
        if (mediaStream) {
            // бһ”бһүбҹ’бһҲбһ”бҹӢбһҖбһ¶бһҡбһ…бҹӮбһҖбһҡбҹҶбһӣбҹӮбһҖ
            await mediaStream.stopShare();
            console.log("рҹӣ‘ бһ”бһ¶бһ“бһ”бһүбҹ’бһҲбһ”бҹӢбһҖбһ¶бһҡбһ…бҹӮбһҖбһҡбҹҶбһӣбҹӮбһҖбһўбҹҒбһҖбҹ’бһҡбһ„бҹӢбҹ”");
        }
    } catch (error) {
        console.error("бһҳбһ¶бһ“бһ”бһүбҹ’бһ бһ¶бһҖбҹ’бһ“бһ»бһ„бһҖбһ¶бһҡбһ”бһүбҹ’бһҲбһ”бҹӢбһҖбһ¶бһҡбһ…бҹӮбһҖбһҡбҹҶбһӣбҹӮбһҖбһўбҹҒбһҖбҹ’бһҡбһ„бҹӢбҹ–", error);
    }
}

export { startScreenSharing, stopScreenSharing };
