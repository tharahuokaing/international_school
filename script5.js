// бһ“бһ¶бҹҶбһ…бһјбһӣ (Import) бһҳбҹүбһјбһҢбһ»бһӣ Zoom Video SDK
import ZoomVideo from '@zoom/videosdk';

// бһ”бһ„бҹ’бһҖбһҫбһҸ Client бһҹбһҳбҹ’бһҡбһ¶бһ”бҹӢбһӮбҹ’бһҡбһ”бҹӢбһӮбҹ’бһҡбһ„бһҖбһ¶бһҡбһ—бҹ’бһҮбһ¶бһ”бҹӢ
const client = ZoomVideo.createClient();
let mediaStream;

/**
 * бһҳбһ»бһҒбһ„бһ¶бһҡбһҹбһҳбҹ’бһҡбһ¶бһ”бҹӢбһ…бһ¶бһ”бҹӢбһ•бҹ’бһҸбһҫбһҳ бһ“бһ·бһ„бһ…бһјбһӣбһҡбһҪбһҳбһҗбҹ’бһ“бһ¶бһҖбҹӢбһҡбҹҖбһ“бһўбһ“бһЎбһ¶бһү
 * @param {string} topic - бһҲбҹ’бһҳбҹ„бҹҮбһ”бһ“бҹ’бһ‘бһ”бҹӢбһҡбҹҖбһ“ бһ¬бһңбһ·бһүбҹ’бһүбһ¶бһҹбһ¶бһҹбһ·бһҖбҹ’бһҹбһ¶
 * @param {string} token - бһӣбҹҒбһҒбһҖбһјбһҠбһҹбһ»бһңбһҸбҹ’бһҗбһ·бһ—бһ¶бһ– JWT Token бһҹбһҳбҹ’бһҡбһ¶бһ”бҹӢбһ•бҹ’бһ‘бҹҖбһ„бһ•бҹ’бһ‘бһ¶бһҸбҹӢ
 * @param {string} studentName - бһҲбҹ’бһҳбҹ„бҹҮбһҹбһ·бһҹбҹ’бһҹ бһ¬бһўбҹ’бһ“бһҖбһ”бҹ’бһҡбһҫбһ”бҹ’бһҡбһ¶бһҹбҹӢбһҠбҹӮбһӣбһҸбҹ’бһҡбһјбһңбһ…бһјбһӣбһҡбһҪбһҳ
 */
async function startClassroom(topic, token, studentName) {
    try {
        // бҹЎ. бһ…бһ¶бһ”бҹӢбһ•бҹ’бһҸбһҫбһҳбһҠбҹҶбһҺбһҫбһҡбһҖбһ¶бһҡ Global Client бһҡбһ”бһҹбҹӢ Zoom
        await client.init('en-US', 'Global', { patchJsMedia: true });
        console.log("Zoom Video SDK бһ”бһ¶бһ“бһ…бһ¶бһ”бҹӢбһ•бҹ’бһҸбһҫбһҳбһҠбҹ„бһҷбһҮбҹ„бһӮбһҮбҹҗбһҷбҹ”");

        // бҹў. бһ…бһјбһӣбһҡбһҪбһҳбһ‘бҹ…бһҖбһ¶бһ“бҹӢбһ”бһ“бҹ’бһ‘бһ”бҹӢбһҡбҹҖбһ“ (Classroom Channel) бһҠбҹӮбһӣбһ”бһ¶бһ“бһҖбҹҶбһҺбһҸбҹӢ
        await client.join(topic, token, studentName);
        console.log(`бһ”бһ¶бһ“бһ…бһјбһӣбһҡбһҪбһҳбһҗбҹ’бһ“бһ¶бһҖбҹӢбһҡбҹҖбһ“: ${topic} бһҖбҹ’бһ“бһ»бһ„бһ“бһ¶бһҳбһҮбһ¶: ${studentName}`);

        // бҹЈ. бһ”бһ„бҹ’бһҖбһҫбһҸбһ”бҹ’бһҡбһ–бҹҗбһ“бҹ’бһ’бһ•бҹ’бһҹбһ¶бһҷ (Media Pipeline) бһҹбһҳбҹ’бһҡбһ¶бһ”бҹӢбһҹбҹҶбһЎбҹҒбһ„ бһ“бһ·бһ„бһңбһёбһҠбҹҒбһўбһј
        mediaStream = client.getMediaStream();

        // бҹӨ. бһ”бһҫбһҖбһҠбҹҶбһҺбһҫбһҡбһҖбһ¶бһҡбһҖбһ¶бһҳбҹҒбһҡбҹүбһ¶ бһ“бһ·бһ„бһҳбһёбһҖбҹ’бһҡбһјбһ бҹ’бһңбһјбһ“
        await mediaStream.startVideo();
        await mediaStream.startAudio();
        console.log("бһҖбһ¶бһҳбҹҒбһҡбҹүбһ¶ бһ“бһ·бһ„бһҳбһёбһҖбҹ’бһҡбһјбһ бҹ’бһңбһјбһ“бһҸбҹ’бһҡбһјбһңбһ”бһ¶бһ“бһ”бһҫбһҖбһҠбҹҶбһҺбһҫбһҡбһҖбһ¶бһҡбһҮбҹ„бһӮбһҮбҹҗбһҷбҹ”");

    } catch (error) {
        // бһ”бһ„бҹ’бһ бһ¶бһүбһҹбһ¶бһҡбһ–бҹ’бһҡбһҳбһ¶бһ“бһҖбҹ’бһ“бһ»бһ„бһҖбһҡбһҺбһёбһҳбһ¶бһ“бһҖбҹҶбһ бһ»бһҹбһ”бһ…бҹ’бһ…бҹҒбһҖбһ‘бҹҒбһҹ
        console.error("бһҖбһ¶бһҡбһ—бҹ’бһҮбһ¶бһ”бҹӢбһ‘бҹ…бһҖбһ¶бһ“бҹӢбһҗбҹ’бһ“бһ¶бһҖбҹӢбһҡбҹҖбһ“бһҳбһ¶бһ“бһ”бһүбҹ’бһ бһ¶:", error);
    }
}

// бһ“бһ¶бҹҶбһ…бҹҒбһүбһҳбһ»бһҒбһ„бһ¶бһҡбһҠбһҫбһҳбҹ’бһ”бһёбһҷбһҖбһ‘бҹ…бһ”бҹ’бһҡбһҫбһ”бҹ’бһҡбһ¶бһҹбҹӢбһҖбҹ’бһ“бһ»бһ„бһҜбһҖбһҹбһ¶бһҡбһ•бҹ’бһҹбҹҒбһ„бһ‘бҹҖбһҸ (бһ§бһ‘бһ¶бһ бһҡбһҺбҹҚбҹ– бһ…бһ»бһ…бһ”бҹҠбһјбһҸбһ»бһ„бһҠбһҫбһҳбҹ’бһ”бһёбһ…бһјбһӣбһҡбҹҖбһ“)
export { startClassroom };
